#include "push_swap.h"

int	is_number(const char *ln)
{
	int	i;

	i = 1;
	if (ln[0] < '0' && ln[0] > '9' && ln[0] != '-')
		return (0);
	if (ln[0] == '-')
		i++;
	while (ln[i])
	{
		if (ln[i] < '0' && ln[i] > '9')
			return (0);
		i++;
	}
	return (1);
}
